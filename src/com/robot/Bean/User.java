package com.robot.Bean;

public class User {
	private String UserName;//用户昵称
	private Integer Usericon;//用户头像
	public String getUserName() {
		return UserName;
	}
	public void setUserName(String userName) {
		UserName = userName;
	}
	public Integer getUsericon() {
		return Usericon;
	}
	public void setUsericon(Integer usericon) {
		Usericon = usericon;
	}
	
	

}
